public class Node {
    private int numero;
    private Node proximo;

    public int getNumero() {
        return numero;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }
    public Node getProximo() {
        return proximo;
    }
    public void setProximo(Node node) {
        this.proximo = node;
    } 
}